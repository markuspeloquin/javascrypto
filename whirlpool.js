// bytes in 'bit_count'
Whirlpool._LENGTH_BYTES = 32;

// the number of rounds of the internal dedicated block cipher
Whirlpool.ROUNDS = 10;

// the core Whirlpool transform
Whirlpool.prototype.process_buffer = function()
{
	var K = new Array(8);		// the round key
	var block = Buffer.zeros64(8);	// mu(buffer)
	var state = new Array(8);	// the cipher state
	var L = new Array(8);
	var i, j;

	// map the buffer to a block
	Buffer.copy(block, 0, this._buf, 0, Whirlpool.BLOCK);

	// compute and apply K^0 to the cipher state
	for (i = 0; i < 8; i++) {
		K[i] = new Long(this._hash[i]);
		state[i] = Long.xor(block[i], K[i]);
	}

	var Wp = Whirlpool;

	// iterate over all rounds
	for (i = 1; i <= Whirlpool.ROUNDS; i++) {
		// compute K^i from K^{i-1}
		L[0] = Long.xor_list([
		    Wp.c_0[K[0].get8(7)],Wp.c_1[K[7].get8(6)],
		    Wp.c_2[K[6].get8(5)],Wp.c_3[K[5].get8(4)],
		    Wp.c_4[K[4].get8(3)],Wp.c_5[K[3].get8(2)],
		    Wp.c_6[K[2].get8(1)],Wp.c_7[K[1].get8(0)],Wp.rc[i]]);
		L[1] = Long.xor_list([
		    Wp.c_0[K[1].get8(7)],Wp.c_1[K[0].get8(6)],
		    Wp.c_2[K[7].get8(5)],Wp.c_3[K[6].get8(4)],
		    Wp.c_4[K[5].get8(3)],Wp.c_5[K[4].get8(2)],
		    Wp.c_6[K[3].get8(1)],Wp.c_7[K[2].get8(0)]]);
		L[2] = Long.xor_list([
		    Wp.c_0[K[2].get8(7)],Wp.c_1[K[1].get8(6)],
		    Wp.c_2[K[0].get8(5)],Wp.c_3[K[7].get8(4)],
		    Wp.c_4[K[6].get8(3)],Wp.c_5[K[5].get8(2)],
		    Wp.c_6[K[4].get8(1)],Wp.c_7[K[3].get8(0)]]);
		L[3] = Long.xor_list([
		    Wp.c_0[K[3].get8(7)],Wp.c_1[K[2].get8(6)],
		    Wp.c_2[K[1].get8(5)],Wp.c_3[K[0].get8(4)],
		    Wp.c_4[K[7].get8(3)],Wp.c_5[K[6].get8(2)],
		    Wp.c_6[K[5].get8(1)],Wp.c_7[K[4].get8(0)]]);
		L[4] = Long.xor_list([
		    Wp.c_0[K[4].get8(7)],Wp.c_1[K[3].get8(6)],
		    Wp.c_2[K[2].get8(5)],Wp.c_3[K[1].get8(4)],
		    Wp.c_4[K[0].get8(3)],Wp.c_5[K[7].get8(2)],
		    Wp.c_6[K[6].get8(1)],Wp.c_7[K[5].get8(0)]]);
		L[5] = Long.xor_list([
		    Wp.c_0[K[5].get8(7)],Wp.c_1[K[4].get8(6)],
		    Wp.c_2[K[3].get8(5)],Wp.c_3[K[2].get8(4)],
		    Wp.c_4[K[1].get8(3)],Wp.c_5[K[0].get8(2)],
		    Wp.c_6[K[7].get8(1)],Wp.c_7[K[6].get8(0)]]);
		L[6] = Long.xor_list([
		    Wp.c_0[K[6].get8(7)],Wp.c_1[K[5].get8(6)],
		    Wp.c_2[K[4].get8(5)],Wp.c_3[K[3].get8(4)],
		    Wp.c_4[K[2].get8(3)],Wp.c_5[K[1].get8(2)],
		    Wp.c_6[K[0].get8(1)],Wp.c_7[K[7].get8(0)]]);
		L[7] = Long.xor_list([
		    Wp.c_0[K[7].get8(7)],Wp.c_1[K[6].get8(6)],
		    Wp.c_2[K[5].get8(5)],Wp.c_3[K[4].get8(4)],
		    Wp.c_4[K[3].get8(3)],Wp.c_5[K[2].get8(2)],
		    Wp.c_6[K[1].get8(1)],Wp.c_7[K[0].get8(0)]]);
		for (j = 0; j < 8; j++) K[j] = L[j];
		// apply the i-th round transformation
		L[0] = Long.xor_list([
		    Wp.c_0[state[0].get8(7)],Wp.c_1[state[7].get8(6)],
		    Wp.c_2[state[6].get8(5)],Wp.c_3[state[5].get8(4)],
		    Wp.c_4[state[4].get8(3)],Wp.c_5[state[3].get8(2)],
		    Wp.c_6[state[2].get8(1)],Wp.c_7[state[1].get8(0)],K[0]]);
		L[1] = Long.xor_list([
		    Wp.c_0[state[1].get8(7)],Wp.c_1[state[0].get8(6)],
		    Wp.c_2[state[7].get8(5)],Wp.c_3[state[6].get8(4)],
		    Wp.c_4[state[5].get8(3)],Wp.c_5[state[4].get8(2)],
		    Wp.c_6[state[3].get8(1)],Wp.c_7[state[2].get8(0)],K[1]]);
		L[2] = Long.xor_list([
		    Wp.c_0[state[2].get8(7)],Wp.c_1[state[1].get8(6)],
		    Wp.c_2[state[0].get8(5)],Wp.c_3[state[7].get8(4)],
		    Wp.c_4[state[6].get8(3)],Wp.c_5[state[5].get8(2)],
		    Wp.c_6[state[4].get8(1)],Wp.c_7[state[3].get8(0)],K[2]]);
		L[3] = Long.xor_list([
		    Wp.c_0[state[3].get8(7)],Wp.c_1[state[2].get8(6)],
		    Wp.c_2[state[1].get8(5)],Wp.c_3[state[0].get8(4)],
		    Wp.c_4[state[7].get8(3)],Wp.c_5[state[6].get8(2)],
		    Wp.c_6[state[5].get8(1)],Wp.c_7[state[4].get8(0)],K[3]]);
		L[4] = Long.xor_list([
		    Wp.c_0[state[4].get8(7)],Wp.c_1[state[3].get8(6)],
		    Wp.c_2[state[2].get8(5)],Wp.c_3[state[1].get8(4)],
		    Wp.c_4[state[0].get8(3)],Wp.c_5[state[7].get8(2)],
		    Wp.c_6[state[6].get8(1)],Wp.c_7[state[5].get8(0)],K[4]]);
		L[5] = Long.xor_list([
		    Wp.c_0[state[5].get8(7)],Wp.c_1[state[4].get8(6)],
		    Wp.c_2[state[3].get8(5)],Wp.c_3[state[2].get8(4)],
		    Wp.c_4[state[1].get8(3)],Wp.c_5[state[0].get8(2)],
		    Wp.c_6[state[7].get8(1)],Wp.c_7[state[6].get8(0)],K[5]]);
		L[6] = Long.xor_list([
		    Wp.c_0[state[6].get8(7)],Wp.c_1[state[5].get8(6)],
		    Wp.c_2[state[4].get8(5)],Wp.c_3[state[3].get8(4)],
		    Wp.c_4[state[2].get8(3)],Wp.c_5[state[1].get8(2)],
		    Wp.c_6[state[0].get8(1)],Wp.c_7[state[7].get8(0)],K[6]]);
		L[7] = Long.xor_list([
		    Wp.c_0[state[7].get8(7)],Wp.c_1[state[6].get8(6)],
		    Wp.c_2[state[5].get8(5)],Wp.c_3[state[4].get8(4)],
		    Wp.c_4[state[3].get8(3)],Wp.c_5[state[2].get8(2)],
		    Wp.c_6[state[1].get8(1)],Wp.c_7[state[0].get8(0)],K[7]]);
		for (j = 0; j < 8; j++) state[j] = L[j];
	}
	// apply the Miyaguchi-Preneel compression function
	for (i = 0; i < 8; i++)
		this._hash[i] = Long.xor_list([
		    this._hash[i], state[i], block[i]]);
}

Whirlpool.prototype._increment_count = function(sz)
{
	var val = Long.lshift(new Long(sz),3);
	var sum;
	for (var i = 15; i >= 0; i--) {
		sum = val.get16(0) + this._bit_count[i];
		this._bit_count[i] = sum & 0xffff;
		val = Long.rshift(Long.add(val,new Long(sum)),16);
	}
}

// append bit length of hashed data
Whirlpool.prototype._append_count = function()
{
	// compress bit_count to compr
	var compr = new Array(8);
	for (var i = 0; i < 8; i++)
		compr[i] = (this._bit_count[i<<1] << 16) |
		    this._bit_count[(i<<1) + 1];
	Buffer.copy(this._buf, this._pos, compr, 0, Whirlpool._LENGTH_BYTES);
}
