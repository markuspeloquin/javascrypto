Whirlpool = function()
{
	this.constructor = Whirlpool;
	// hashing state
	this._hash = new Array(Whirlpool.DIGEST>>>3);
	// number of hashed bits (each a 16-bit integer)
	this._bit_count = new Array(16);
	// data to hash
	this._buf = Buffer.zeros32(Whirlpool.BLOCK>>>2);
}
Whirlpool.BLOCK = 64;
Whirlpool.DIGEST = 64;
Whirlpool.prototype = new Hash();

/** Initialize/reset a whirlpool context. */
Whirlpool.prototype.init = function()
{
	Buffer.setzero64(this._hash);
	Buffer.setzero32(this._bit_count);
	// pos in buf
	this._pos = 0;
}

/** Add data to the computation.
 * \param buf	The data to be appended (big endian).
 * \param sz	Size of data in bytes.
 */
Whirlpool.prototype.update = function(buf, sz)
{
	// this function maintains the invariant: pos < WBLOCKBYTES

	this._increment_count(sz);

	if (this._pos + sz < Whirlpool.BLOCK) {
		// buffer will not fill
		Buffer.copy(this._buf, this._pos, buf, 0, sz);
		this._pos += sz;
		return;
	}

	// do first (full) block
	var bytes = Whirlpool.BLOCK - this._pos;
	Buffer.copy(this._buf, this._pos, buf, 0, bytes);
	this.process_buffer();
	var srcpos = bytes;
	sz -= bytes;

	// do subsequent (full) blocks
	while (sz >= Whirlpool.BLOCK) {
		Buffer.copy(this._buf, 0, buf, srcpos, Whirlpool.BLOCK);
		this.process_buffer();
		srcpos += Whirlpool.BLOCK;
		sz -= Whirlpool.BLOCK;
	}

	// copy next partial block for later
	if (sz)
		Buffer.copy(this._buf, 0, buf, srcpos, sz);
	this._pos = sz;
}

/** Finish computation
 * \return The digest
 */
Whirlpool.prototype.end = function()
{
	var i;
	// this function uses the invariant: pos < WBLOCKBYTES

	// append a '1'-bit
	Buffer.set32(this._buf, this._pos++, 0x80);

	// pad with zero bits to complete (N*WBLOCKBYTES - LENGTHBYTES) bits

	if (this._pos + Whirlpool._LENGTH_BYTES > Whirlpool.BLOCK) {
		// no room to fit the bit count; that will go in the next
		// block
		if (this._pos != Whirlpool.BLOCK)
			// fill remainder with zeros
			for (i = this._pos; i < Whirlpool.BLOCK; i++)
				Buffer.set32(this._buf, i, 0);
		this.process_buffer();
		this._pos = 0;
	}
	if (this._pos + Whirlpool._LENGTH_BYTES != Whirlpool.BLOCK)
		// pad middle with zeros
		while (this._pos < Whirlpool.BLOCK - Whirlpool._LENGTH_BYTES)
			Buffer.set32(this._buf, this._pos++, 0);

	this._append_count();

	// process final data block
	this.process_buffer();

	// return digest
	var res = new Array(this._hash.length);
	for (i = 0; i < this._hash.length; i++) res[i] = this._hash[i];
	return res;
}

